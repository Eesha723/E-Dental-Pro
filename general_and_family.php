              When you’re needing general dental treatment that is professional, caring, experienced, and gentle, then look no further than eDental.

With five treatment rooms on-site, we can normally provide same-day treatment for your dental requirements.

General Dental Treatments We Provide
Clean & Scale
Perhaps the most common dental procedure, it’s also one that allows us to get a glimpse of your oral health and alert us to any potential issues. Our highly experienced dental hygienists will expertly clean, scale, and polish your teeth, and have them looking in tip-top shape. You’ll step out of the chair and have a smile from ear to ear.

Oral Hygiene (Including Fluoride Treatment)
We’ll be looking for plaque, calculus, and staining too. While correct, regular brushing can keep plaque at bay, calculus and staining may need some help from us. We’ll also be able to provide you with a fluoride treatment to keep your teeth nice and strong.

Chipped Tooth Management
Sometimes, for no particular reason, you may experience a chipped tooth. Don’t panic, as we can usually help and get your tooth back to health, quickly. If you notice a chipped tooth, then the first thing you need to do is to book an appointment with us and see a Dentist as soon as possible. If you don’t then you’ll risk causing further damage and possibly losing the entire tooth (and none of us wants that).

Toothache
If you’ve ever experienced a toothache, then you’ll know the pain and discomfort it can cause. A toothache can be caused by all sorts of conditions, and it’s important that you book an appointment and see a Dentist as soon as possible. We can usually fit you in to see a Dentist on the same day.

X-Ray (Various Options Available)
Whether you need an X-Ray as part of your normal dental treatment plan, if you have a requirement for a specific treatment you’ll be undertaking, eDental has a variety of X-Ray options on hand. Each of our dental treatment rooms has an ‘in-chair’ X-ray machine, for your convenience. We also have specialised X-Ray machines for more detailed procedures such as Smile Design. Please contact us to ask about any X-Ray’s that you may need.

Treatment Plans for Gum Disease/Bad Breath
Sometimes, gum disease and bad breath can strike even the most careful brusher and flosser. If you are experiencing bleeding, soreness in the gums, or bad breath, then it’s important to book an appointment and see a Dentist, to get to the bottom of what is going on in your mouth. Our expert team will uncover the cause, and be able to provide a treatment plan to manage it properly.

General Dental Treatment, Perth’s Favorite Dental Practice
If you’re looking for a new Dentist for your general dental requirements, then look no further than the friendly and caring team at eDental. Open 7 days a week (including all public holidays), from 8 am – 8 pm*, we can normally see you on the same day or at short notice.

From the moment you drive, we’ll put your mind at ease, and provide you with a professional, caring, yet relaxed dental experience.

