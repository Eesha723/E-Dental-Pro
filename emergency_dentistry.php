See a Dentist Now For Removal. Pain, discomfort, and an ache that is felt all through your body. That’s just how much of a (literally) headache wisdom teeth can cause you.

If you are experiencing any pain or discomfort in your wisdom teeth area, then it’s important that you see a suitably qualified Dentist so that they can give you the proper treatment plan that you deserve.

Wisdom Teeth Removal in Perth
wisdom tooth removal
Before any treatments are discussed or planned, we’ll take a panoramic X-Ray to assess the positioning of your wisdom teeth and whether they could prove to be problematic. Fortunately, at eDental, we have an on-site OPG so you can have any required X-Rays taken on the day of your initial consultation.

Once we have taken this X-Ray image, we can determine if your wisdom teeth need to be removed or not.

We find that in most wisdom teeth cases, we can treat them at our purpose-built surgery in Rivervale, rather than sending you to a hospital.

In certain cases, you may require a referral to an oral surgeon for treatment under a general anaesthetic. If this is the case for you, then pain relief medications or oral antibiotics can be prescribed as an interim measure.

Removal of Wisdom Teeth Can Be The Best Plan of Defence
Many of us have our wisdom teeth removed in young adulthood, as this prevents unwanted problems and issues from occurring later in life and from impacting our surrounding teeth.

