For many of us, from time to time in our life we will need a filling in order to restore our teeth to their original anatomy.

While some of us may have childhood memories of ‘the drill’, a procedure these days is a much more pleasant, pain-free experience.

At eDental, we place a large emphasis on providing pain-free dental fillings and making your treatment as quick and as comfortable as possible.

How Do I Know If I Need A Filling?
Only your dentist will be able to tell you if you need a filling or not. Below are some of the most common reasons why you would need one;

You have a cavity that requires prompt attention
Small holes in the teeth
You have a fractured tooth
Discoloured teeth may benefit from a filling
If your teeth have loss structure and need additional support
What Type Of Fillings Does eDental Offer?
The main material that we use here at eDental, is a composite restoration. This is a mixture of plastic material and tiny glass particles. Unlike the lesser-used amalgam, composite fillings can be matched to the natural colour of your teeth.

Composite material sets quickly, and bonds it to the tooth’s natural surface.

Who Will Conduct The Filling Treatment?
Only a qualified and experienced eDental Dentist will perform this procedure, with the assistance of a dedicated dental nurse.

If you require a filling, then your Dentist will normally find this out when you’re receiving your six-monthly check-ups. They would advise you at the time, and make an appointment for you to come back for treatment in the near future.
