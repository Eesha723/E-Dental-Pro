Need Root Canal Treatment in Perth?
If you have a tooth (or teeth) that have been damaged by decay, periodontal disease, or trauma, then you may find that you will benefit from a Root Canal Treatment. A Root Canal treatment is used when a simple filling is not adequate.

If you’re suffering from an acute toothache, then the good news is that this is often resolved immediately after your very first Root Canal treatment.

How Do I Know If I Need Root Canal Treatment?
While you will need to discuss with one of our Dentists if you do require Root Canal treatment, the below are some common ailments where Root Canal may be required;

Recurrent Decay Under a Filling, Crown, or Veneer
Deep Decay
Trauma
Periodontal/Gum Disease
Tooth/Enamel Wear
Prior Extensive Dental Work on a Single Tooth
Now The Root Has Been Saved, What Next?
Now that we have successfully saved your tooth’s root, it’s highly recommended that you have a Crown placed over the top. As the tooth could become brittle and prone to fracture, a Crown acts as a strong helmet, protecting the tooth in question.

Saving Your Teeth Is The Highest Priority
Conducting a Root Canal treatment is a way of saving your tooth, and we always try and save a tooth if we possibly can.

Firstly, to avoid infection, we’ll begin by treating the nerve under the affected tooth with an antibiotic.

Root Canal Therapy, A Permanent Solution For Your Tooth
In most cases of Root Canal treatment, the procedure is an overwhelming success. The chances are that you’ll never experience any further issues or pain in that tooth, ever again. Your dentist will discuss with you a thorough treatment plan and detailed instructions for the ongoing care of the tooth.

To find out further information on our Root Canal treatment, then please call us on 1300 467 112 , or fill in the form and one of our friendly team will shortly be in touch.
